const INTL_LOCALE: Record<Locale, string> = {
  en: "en-US",
  sv: "sv-SE",
};

/** Maps our app Locale ("en"/"sv") to a full Intl/BCP-47 locale tag for number/date formatting. */
export function intlLocale(locale: Locale): string {
  return INTL_LOCALE[locale];
}

